package Week2Solutions;

public class SwapNumbers {

    public static void main(String[] args) {
        int x = 20;
        int y = 10;
        x = x + y; //20+10=30
        y = x - y; //30-10=20
        x = x - y; //30-20=105



        System.out.println(x + "" + y);

    }

}
